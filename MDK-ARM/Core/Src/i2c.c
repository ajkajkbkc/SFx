/**
  * @brief  向OLED写入命令
  * @param  I2C_Command：命令代码
  * @retval 无
  */
static void WriteCmd(unsigned char I2C_Command)
{
    HAL_I2C_Mem_Write(&hi2c1, OLED_I2C_ADDRESS, 0x00, I2C_MEMADD_SIZE_8BIT, &I2C_Command, 1, 0x100);
}
